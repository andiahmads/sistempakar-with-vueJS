<p class="para">

    Narsistik atau biasa dikenal sebagai Narcissistic Personality Disorder adalah sebuah gangguan mental. Narcissistic
    Personality Disorder adalah kondisi orang yang menganggap dirinya jauh lebih penting dari orang lain, memiliki
    kebutuhan yang tinggi untuk dipuji atau dibanggakan, namun memiliki empati yang rendah terhadap orang lain.

    Narsisme pertama kali dipopulerkan oleh psikolog ternama, Sigmund Freud, untuk menjelaskan kepribadian seseorang
    yang mengejar pengakuan dari orang lain terhadap kekaguman dan kesombongan egoistik akan ciri pribadinya.

    Akan tetapi dibalik rasa percaya diri yang begitu tinggi sebenarnya ia memiliki rasa percaya diri yang rapuh dan
    mudah runtuh hanya dengan sedikit kritikan. Gangguan ini dapat mempengaruhi berbagai aspek dalam kehidupan apabila
    tidak ditangani secara tepat.
</p>
